package com.ocsr.service;

import com.ocsr.dao.FetchDAO;

public class FetchService {
public String fetchCarMake(){
	FetchDAO DAOobject=new FetchDAO();
	return DAOobject.fetchCarMake();	
}
public String fetchCarModel(String make){
	FetchDAO DAOobject=new FetchDAO();
	return DAOobject.fetchCarModel(make);	
}
public String fetchCarType(String make,String type){
	FetchDAO DAOobject=new FetchDAO();
	return DAOobject.fetchCarType(make,type);	
}
public String fetchAddress(String userid){
	FetchDAO DAOobject=new FetchDAO();
	return DAOobject.fetchAddress(userid);	
}
}
